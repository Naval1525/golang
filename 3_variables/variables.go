package main

import "fmt"

func main() {
	var name = "golang"
	var isAdult = true
	var age int = 12
	fmt.Println(name)
	fmt.Println(isAdult)
	fmt.Println(age)

	//short hand syntax
	name_1 := "golang"
	isAdult_1 := true
	age_1 := 12
	fmt.Println(name_1)
	fmt.Println(isAdult_1)
	fmt.Println(age_1)

}
